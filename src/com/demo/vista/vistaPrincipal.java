            Producto m = (Producto) it.next();

            Object[] fila = new Object[11];

            fila[0] = m.getIdProducto();
            fila[1] = m.getMarca().getDescripcion();
            fila[2] = m.getPresentacion().getDescripcion();
            fila[3] = m.getProveedor().getDescripcion();
